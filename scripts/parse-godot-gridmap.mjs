/**
 * Extracts the GridMap tile layout from a Godot .tscn scene
 * (run with: node scripts/parse-godot-gridmap.mjs <main.tscn> <out.ts>).
 *
 * Godot stores cells as int32 triples (a, b, v):
 *   a = cell x (int16, low bits), b = cell z (int16, low bits),
 *   v = meshLibraryItem | (orientationIndex << 16)
 * Item ids follow the kit's mesh-library.tres. Orientation indices are Godot
 * orthogonal-basis indices; the game resolves them to yaws by constraint
 * solving (see systems/gridTrack.ts), so they're passed through raw.
 */
import { readFileSync, writeFileSync } from 'node:fs'

const [, , input, output] = process.argv
if (!input || !output) {
  console.error('usage: node scripts/parse-godot-gridmap.mjs <main.tscn> <out.ts>')
  process.exit(1)
}

const ITEM_NAMES = {
  0: 'empty',
  1: 'forest',
  2: 'tents',
  3: 'corner',
  4: 'finish',
  5: 'ramp',
  6: 'straight',
}

const src = readFileSync(input, 'utf8')
const m = src.match(/"cells":\s*PackedInt32Array\(([^)]*)\)/)
if (!m) throw new Error('no GridMap cells found')
const ints = m[1].split(',').map((s) => Number(s.trim()))

const toInt16 = (u) => (u & 0x8000 ? u - 0x10000 : u)
const cells = []
for (let i = 0; i < ints.length; i += 3) {
  const x = toInt16(ints[i] & 0xffff)
  const z = toInt16(ints[i + 1] & 0xffff)
  const item = ints[i + 2] & 0xffff
  const orient = (ints[i + 2] >> 16) & 0x1f
  const model = ITEM_NAMES[item]
  if (!model) throw new Error(`unknown mesh-library item ${item} at (${x},${z})`)
  cells.push([x, z, model, orient])
}

writeFileSync(
  output,
  `// Generated by scripts/parse-godot-gridmap.mjs from Kenney's Starter Kit Racing — do not edit.\n` +
    `// [cellX, cellZ, model, godotOrientationIndex]\n` +
    `import type { GridCellData } from '../../systems/gridTrack'\n\n` +
    `export const STARTER_GP_CELLS: GridCellData[] = ${JSON.stringify(cells)}\n`,
)
console.log(`wrote ${cells.length} cells (${cells.filter((c) => ['straight', 'corner', 'finish', 'ramp'].includes(c[2])).length} road tiles)`)
