/**
 * One-off extraction for mesh-based tracks (run with: node scripts/extract-track-centerline.mjs <in.glb> <out.ts>).
 *
 * Assumes the road mesh ("Object_0") is a curve-extruded box ribbon with
 * row-major vertex order (N vertices per cross-section). Emits a TS module
 * with the downsampled centerline as [x, topY, z] tuples.
 *
 * Requires @gltf-transform/core (devDependency).
 */
import { writeFileSync } from 'node:fs'
import { NodeIO } from '@gltf-transform/core'

const [, , input, output] = process.argv
if (!input || !output) {
  console.error('usage: node scripts/extract-track-centerline.mjs <in.glb> <out.ts>')
  process.exit(1)
}

const ROW = 10 // verts per cross-section (incl. duplicates) — verified for this asset
const EVERY = 3 // keep every 3rd cross-section (~3.9m spacing)

const doc = await new NodeIO().read(input)
const mesh = doc.getRoot().listMeshes().find((m) => m.getName() === 'Object_0')
const pos = mesh.listPrimitives()[0].getAttribute('POSITION')
const rows = Math.floor(pos.getCount() / ROW)

const points = []
const v = []
for (let r = 0; r < rows; r += EVERY) {
  let sx = 0
  let sz = 0
  let topY = -Infinity
  for (let i = 0; i < ROW; i++) {
    pos.getElement(r * ROW + i, v)
    sx += v[0]
    sz += v[2]
    topY = Math.max(topY, v[1])
  }
  points.push([sx / ROW, topY, sz / ROW])
}

// The vertex sequence may keep going after completing the loop (this asset
// re-traces ~1.4km of road with a second strip). Truncate at the first point
// past halfway that comes back to the start.
const [fx, , fz] = points[0]
let closeAt = -1
for (let i = Math.floor(points.length / 2); i < points.length; i++) {
  if (Math.hypot(points[i][0] - fx, points[i][2] - fz) < 3) {
    closeAt = i
    break
  }
}
if (closeAt > 0) points.length = closeAt
// drop a trailing point that duplicates the start (closed loop)
const [lx, , lz] = points[points.length - 1]
if (Math.hypot(fx - lx, fz - lz) < 2) points.pop()

const rounded = points.map(([x, y, z]) => [x, y, z].map((n) => Math.round(n * 100) / 100))
writeFileSync(
  output,
  `// Generated by scripts/extract-track-centerline.mjs — do not edit by hand.\n` +
    `// Source: ${input.split('/').pop()} (road surface centerline, [x, topY, z], ~${(EVERY * 1.3).toFixed(1)}m spacing)\n` +
    `export const GRAND_CIRCUIT_CENTERLINE: [number, number, number][] = ${JSON.stringify(rounded)}\n`,
)
console.log(`wrote ${rounded.length} centerline points to ${output}`)
